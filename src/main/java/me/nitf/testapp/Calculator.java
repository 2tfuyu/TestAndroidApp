package me.nitf.testapp;

class Calculator {

    int plus(int tmp, int i) {
        return (tmp + i);
    }

    int minus(int tmp, int i) {
        return (tmp - i);
    }

    int mul(int tmp, int i) {
        return (tmp * i);
    }

    int div(int tmp, int i) {
        return  (tmp / i);
    }
}
